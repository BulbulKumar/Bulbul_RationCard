<h1>Welcome ration card</h1>
<p>Ration card in India is popular document of address and identity proof, that makes indian households eligible to purchase subsidised food grain from the Public Distribution System. e-ration card is a seamless facility provided by several state governments for households to obtain ration card.

e-ration was first introduced in Delhi and has gradually covered states such as Tamil Nadu and Karnataka. Only aadhaar card holders can apply using e-ration card facility.

